import { readFileSync, writeFileSync, readdirSync, mkdirSync, rmSync, existsSync } from "fs";
import { resolve, dirname, join } from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const ROOT_FILE = "roverctl.md";

const srcDir = resolve(__dirname, "../../rover-ctl/docs");
const destDir = resolve(__dirname, "../docs/reference/roverctl");

if (!existsSync(srcDir)) {
  console.error(
    `Error: Source directory not found: ${srcDir}\n` +
      `Make sure the rover-ctl CLI docs have been generated (see rover-ctl/README.md).`
  );
  process.exit(1);
}

console.log("Generating CLI docs...");

// Clean and recreate destination directory
if (existsSync(destDir)) {
  rmSync(destDir, { recursive: true });
}
mkdirSync(destDir, { recursive: true });

// Discover all .md files in the source directory
const mdFiles = readdirSync(srcDir).filter((f) => f.endsWith(".md"));

/**
 * Determine sidebar_position: the root command gets 1, the rest are sorted
 * alphabetically and numbered from 2 onward.
 */
function buildPositionMap(files) {
  const rest = files.filter((f) => f !== ROOT_FILE).sort();
  const map = new Map();
  map.set(ROOT_FILE, 1);
  rest.forEach((f, i) => map.set(f, i + 2));
  return map;
}

const positionMap = buildPositionMap(mdFiles);

/**
 * Extract the title from the first `## ` heading in the content.
 */
function extractTitle(content) {
  const match = content.match(/^## (.+)$/m);
  return match ? match[1].trim() : "";
}

/**
 * Extract the description from the Synopsis section.
 * Takes the first non-empty line of text after `### Synopsis` and before the
 * next code block or heading.
 */
function extractDescription(content) {
  const synopsisMatch = content.match(/### Synopsis\s*\n([\s\S]*?)(?=\n```|\n#)/);
  if (!synopsisMatch) return "";
  const lines = synopsisMatch[1].split("\n");
  for (const line of lines) {
    const trimmed = line.trim();
    if (trimmed.length > 0) {
      return trimmed;
    }
  }
  return "";
}

/**
 * Transform a single cobra-generated markdown file into a Docusaurus page.
 *
 * @param {string} content - raw markdown content
 * @param {number} sidebarPosition - position in the sidebar
 * @param {boolean} isRoot - true for the root command file (roverctl.md)
 */
function transform(content, sidebarPosition, isRoot) {
  const title = extractTitle(content);
  const description = extractDescription(content);

  // Build frontmatter
  const frontmatter = [
    "---",
    `sidebar_position: ${sidebarPosition}`,
    `title: "${title}"`,
    `description: "${description}"`,
    `generated: true`,
    "---",
    "",
  ].join("\n");

  let transformed = content;

  // Promote the first ## heading to #
  transformed = transformed.replace(/^## (.+)$/m, "# $1");

  // Fix internal links: strip .md extension from link targets
  transformed = transformed.replace(/\]\(([^)]+)\.md\)/g, "]($1)");

  // Fix links pointing to "roverctl" (the root command):
  // In subcommand pages these become broken because the file is now index.md.
  // Replace (roverctl) with (./) but only when it is an exact match — not a
  // prefix like roverctl_apply.
  if (!isRoot) {
    transformed = transformed.replace(/\]\(roverctl\)/g, "](./)");
  }

  // Replace the auto-generated footer with a Docusaurus admonition
  transformed = transformed.replace(
    /^###### Auto generated by spf13\/cobra.*$/m,
    ":::info Auto-generated\nThis page was automatically generated from the Rover-CTL source code.\n:::"
  );

  return frontmatter + transformed;
}

// Process each markdown file
let count = 0;
for (const file of mdFiles) {
  const content = readFileSync(join(srcDir, file), "utf-8");
  const position = positionMap.get(file);
  const isRoot = file === ROOT_FILE;
  const result = transform(content, position, isRoot);

  // The root command file becomes index.md so Docusaurus treats it as the
  // category landing page (avoids slug collision with the directory name).
  const destFile = isRoot ? "index.md" : file;

  writeFileSync(join(destDir, destFile), result, "utf-8");
  console.log(`  Transformed ${file} → ${destFile}`);
  count++;
}

// Write _category_.json
const categoryJson = {
  label: "Rover-CTL",
  position: 2,
  collapsed: true,
  link: {
    type: "doc",
    id: "reference/roverctl/index",
  },
  customProps: {
    description: "Command-line reference for the Rover-CTL tool.",
  },
};
writeFileSync(join(destDir, "_category_.json"), JSON.stringify(categoryJson, null, 2) + "\n", "utf-8");

console.log(`Done! Generated ${count} pages`);
